    public boolean validate(String fn, String ln){
//        String m = "^[A-Z].[a-z]{2,}$";
        if (!fn.matches("^[A-Z].[a-z]{2,}$"))
                firstName.setBackground(new Color(255, 120, 120));
        if (!ln.matches("^[A-Z].[a-z]{2,}$"))
                lastName.setBackground(new Color(255, 120, 120));
        if (fn.matches("^[A-Z].[a-z]{2,}$"))
                firstName.setBackground(null);
        if (ln.matches("^[A-Z].[a-z]{2,}$"))
                lastName.setBackground(null);
        
        return fn.matches("^[A-Z].[a-z]{2,}$") && ln.matches("^[A-Z].[a-z]{2,}$");
    }
