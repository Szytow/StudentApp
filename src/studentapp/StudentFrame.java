    private void addStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStudentActionPerformed
        String studentFirstName = firstName.getText();
        String studentLastName = lastName.getText();  
        while (!validate(studentFirstName, studentLastName)){
             JOptionPane.showMessageDialog(null, "Insert correct student's name");
             return;
        }
        String[] student = {studentFirstName, studentLastName, studentGrade};
        FileWriter fw = null;
        try {
          fw = new FileWriter("students.txt", true);
             } catch (IOException e) { 
           e.printStackTrace();
        }

        BufferedWriter bw = new BufferedWriter(fw);
        try {
          for (int i = 0; i < student.length; i++) {
            bw.write(student[i] + ", ");
          }
          bw.newLine();
        } catch (IOException e) {
           e.printStackTrace();
        }

        try {
           bw.close();
           fw.close();
        } catch (IOException e) {
           e.printStackTrace();
     }
        
        JOptionPane.showMessageDialog(null, "Student Added");
        firstName.setText("");
        lastName.setText("");
    }//GEN-LAST:event_addStudentActionPerformed

